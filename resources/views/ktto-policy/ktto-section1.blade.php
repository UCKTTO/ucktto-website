				The University of the Cordilleras, as an institution engaged towards community development through technology and innovation, recognizes technology transfer as a vital part in utilizing the value of research output, project, and inventions.
			</p>
			<p>
				Technology transfer is the process of systematically transferring intellectual property rights from the University to industries or other entities outside the University. Technology transfer is best achieved through commercialization activities in a manner consistent with both the public interest and the role of the University. Licensing, assignment, sale or donation of Intellectual Property Rights are the significant modes which technology transfer is accomplished.
			</p>
			<p>
				This Policy defines the ownership, protection, distribution of revenue, and commercial exploitation of rights associated with Intellectual Property developed at or received by the University of the Cordilleras and describes the general obligations associated with the technology licensing processes.
