            <h3>SECTION 3</h3>
            <h4 style="text-align: center">DEFINITION OF TERMS</h4>
            
            <p>
                Unless otherwise specified, the following terms shall have the meaning provided in these policies:
            </p>
            <p>
                <b>“Commercialization”</b> is any form of utilization of IP intended to generate value, which may be in the form of a marketable product, process or service, commercial returns, or other benefit to society.  Commercialize is similarly defined.
            </p>
            <p>
                <b>“Creator”</b> is the natural person who has created the work/s and is used interchangeably with “Author”
            </p>
            <p>
                <b>“Inventor”</b> is the natural person who has created a patentable invention under the IP Code of the Philippines.
            </p>
            <p>
                <b>“Employees”</b> refer to academic and non-academic personnel whether part-time, full-time, contractual, or casual employees of the University of the Cordilleras.
            </p>
            <p>
                <b>“Expenses or expense”</b> is the cost of operations incurred in the commercialization of the Intellectual Property. Expenses shall include all patent filing and legal fees, marketing and licensing costs, administrative expenses and fixed overhead costs, and any other expenses necessarily incurred for patenting and commercializing the Intellectual Property.
            </p>
            <p>
                <b>“Mother Unit”</b> shall refer to the college of origination of the work/ works done.
            </p>
            <p>
                <b>“Net Revenue”</b> is defined as the total consideration (including all royalties, fees, and other benefits) received from the commercialization of the Intellectual Property less all expenses incurred in the commercialization of the Intellectual Property. 
            </p>
            <p>
                <b>“Researcher”</b> refers to the employees, students, and partners of the University who perform any research task or participate in any research project administered by the University.
            </p>
            <p>
                <b>“Revenue”</b> refers to all monetary and non-monetary benefits derived as a result of the development, production, transfer, use, and/or commercialization of IPRs, including income from assignments and royalties from licenses.
            </p>
            <p>
                <b>“Spin-off Firm”</b> refers to new companies based on University-developed technologies and other intellectual properties.
            </p>
            <p>
                <b>“Students”</b> shall have the same meaning as defined in the UC Student’s Handbook.
            </p>
            <p>
                <b>“Technology”</b> refers to knowledge and know-how, skills, products, processes, and/or practices.
            </p>
            <p>
                <b>“Technology Transfer”</b> refers to the process by which UC-KTTO systematically transfers to another party the knowledge and technology for the manufacture of a product, the application of a process, or the rendering of a service, which may involve the transfer, assignment, or licensing of IPRs.
            </p>
            <p>
                <b>“Partners”</b> includes but not limited to visiting professors, exchange students, alumni, consultants, and independent contractors, who are undertaking research and other scholarly or creative activities at the University of the Cordilleras.
            </p>
            <p>
                <b>“University”</b> in this context refers to the University of the Cordilleras.
            </p>
            <p>
                <b>“Work/ works”</b> shall mean any kind of intellectual property.
            </p>
