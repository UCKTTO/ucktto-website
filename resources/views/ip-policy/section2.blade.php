			<h3>SECTION 2</h3>
			<h4 style="text-align: center">DEFINITION OF TERMS</h4>
			<p>
				Unless otherwise specified, the following items shall have the meaning provided in these policies.
			</p>
			<p>
				<b>Author.</b> Any person to whom this Policy  is applicable, who individually or jointly with others makes a design, a mark or copyrightable work and who meets the criteria for authorship under the IP laws of the Philippines.
			</p>
			<p>
				<b>Commercialization.</b> Any form of utilization of IP intended to generate value, both for profit and non profit. These may involve exclusive or non-exclusive licensing, assignment of the IP, formation of a start-up company, the use of patent-management companies or government agencies, non-profit use or joint ventures.
			</p>
			<p>
				<b>Commissioned work.</b>  “Commissioned Work” or “Work for Hire” means a work where the University specifically commissions or hires a University employee or any third person to create a work for a fee and which the University on one hand and the inventor, author or third party on the other expressly agree in a written instrument that it be considered as such. The University shall own the intellectual property of the commissioned work, unless otherwise provided in the contract.
			</p>
			<p>
				<b>Copyright.</b> Copyright is a right granted by statute to the author or originator of literary, scholarly, scientific, or artistic productions, including computer programs. Copyright gives him the legal right to determine how the work is used and to obtain economic benefits from the work.
			</p>
			<p>
				<b>Creator.</b> Creator is the natural person who has created the work. The creator can be an invetor, author, artist, designer, developer and other similar designations as defined by law and used in practice.
			</p>
			<p>
				<b>Direct support.</b> Direct support refers to monetary funds/assistance provided by the University to Employee.
			</p>
			<p>
				<b>Employees.</b> Employees refer to academic and non-academic personnel whether part-time, full-time, contractual or casual employees of the University of the Cordilleras.
			</p>
			<p>
				<b>Indirect support.</b> Indirect support refers to monetary funds/assistance provided by organizations outside the University such as grants which are managed or disbursed by the university.
			</p>
			<p>
				<b>Industrial Design.</b> Industrial Design is an aesthetic, ornamental, and non-functional aspect of an article, and may consist of three-dimensional features such as the shape or surface of an article, or of two-dimensional features, such as patterns, lines, or color.
			</p>
			<p>
				<b>Innovations.</b> Innovations refers to the creation of new ideas that result in the development of new or improved products, processes, business models, or services which are then spread, transferred or commercialized. Innovation can be product, a process, or service that is new, original, or improved which creates social, economic or environmental impact.
			</p>
			<p>
				<b>Inventor.</b> Inventor means a person who creates or discovers a new method, form, device, process or composition of matter which is patentable under the law.
			</p>
			<p>
				<b>Invention.</b> Invention means a novel creation, discovery, idea or device that may be patented under the law.
			</p>
			<p>
				<b>IP Policies.</b> IP Policies means the Policies on Intellectual Property of the University of the Cordilleras and its amendments.
			</p>
			<p>
				<b>Intellectual Property (IP).</b> Means any property defined such by the Intellectual Property Code of the Philippines (RA 8293) such as, but not limited to, patents, patentable inventions, utility models, industrial designs, layout designs of integrated circuits, marks, copyright, computer software, technical know-how, trade secrets and plant varieties.
			</p>
			<p>
				<b>Intellectual Property Rights (IPRs).</b>  Refer to those rights recognized and protected in Republic Act No. 8293, otherwise known as the “Intellectual Property Code of the Philippines”, as amended. IPRs shall also include Plant Variety Protection as the term is defined under Title II, Sec 3(j) of Republic Act No. 9168.
			</p>
			<p>
				<b>Knowledge and Technology Transfer Office.</b> The Knowledge and Technology Transfer Office (KTTO) facilitates the exchange of knowledge and technology by fostering, promoting, and enabling university-industry interactions. The KTTO will be the central resource and support hub for researchers and external partners. It is an office that is responsible for IP protection, IP technology transfer, IP commercialization, and IP management.
			</p>
			<p>
				<b>Partners.</b> Partners includes but not limited to visiting professors, exchange students, alumni, consultants, and independent contractors, who are undertaking research and other scholarly or creative activities at the University of the Cordilleras.
			</p>
			<p>
				<b>Patent.</b> Patent is a statutory monopoly which protects the owner against unlicensed use of the patented product or process or any improvement thereof.
			</p>
			<p>
				<b>Research Agreement.</b> Research agreement refers to a contract entered into by RDls and researchers, including the agreements between the RDI and collaborating RDIs.
			</p>
			<p>
				<b>Regularly assigned work.</b>  “Regularly-assigned duties” means the work, duty or responsibility of the university employee as enumerated in the job description or employee/faculty manual, or the regular duties assigned by a superior as part of his/her regular workload. Works submitted to the university as a requirement for continued employment or promotion shall be considered as work done according to the regularly assigned duties of the university employee.  In cases of copyright, however, the ownership of  works submitted by a university employee as a requirement for promotion or research incentive pay shall not be considered as work done according to the regularly-assigned duties of the university employee and shall, therefore,  be owned by the author.  This is subject to the provision on copyright under section 8 of these policies.  
			</p>
			<p>
				<b>Students.</b> Students shall have the same meaning as defined in the UC Student’s Handbook.
			</p>
			<p>
				<b>Substantial Use.</b>  Substantial Use of the facilities and/or resources of the University means either that:
				<ol type="a">
					<li>
						University funds were used to specifically support the development of the intellectual property;
					</li>
					<li>
						The work could not have been developed or created without using the facilities or  resources of the University; or
					</li>
					<li>
						There is use of resources of the University that are not ordinarily available to most  university employees and students such as, but not limited to, laboratories and technical computer software. However, the use of office, library, computers and servers of the university shall not constitute substantial use of University facilities and resources.
					</li>
				</ol>
			</p>
			<p>
				<b>Trademark.</b> Trademark or Mark means a distinctive word, design, symbol, logo or any combination of word and design that identifies and distinguishes the goods or services of one from that of another.
			</p>
			<p>
				<b>University.</b> University in this context refers to the University of the Cordilleras.
			</p>
			<p>
				<b>University Resources.</b> University resources are all resources provided by University of the Cordilleras directly used in the generation of the IP, including work equivalent credit (WEC), office, laboratory, wages and salaries, studio space and equipment; computer hardware, software, and support; secretarial service; research, teaching, and laboratory assistants; supplies; utilities; funding for research and teaching activities, travel; and other funding or reimbursement.
			</p>
			<p>
				<b>Utility Model.</b> Utility Models are inventions that are new and industrially applicable.				
			</p>
			<p>
				<b>Work.</b> Work/works shall mean any kind of intellectual property.				
			</p>
