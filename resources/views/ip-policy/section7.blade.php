			<h3>SECTION 7</h3>
			<h4 style="text-align: center">OWNERSHIP OF PATENT, UTILITY MODEL <br>AND INDUSTRIAL DESIGN</h4>
			
			 <ul>
			 	<li>
			 		<p><b><span>7.1</span> Owned by Inventor</b></p>
			 		<p>Rights to patents shall belong to the inventor except in the instances stated in section 7.2 of this policy.</p>
			 		<p>Notwithstanding ownership of the inventor of his works, the university or any member of the academic community is allowed, without infringing any intellectual property right, free access to and use of their works to pursue or develop them for academic purposes.</p>
			 	</li>
			 	<li>
			 		<p><b><span>7.2</span> Owned by the University</b></p>
			 		<p>Rights to Patent shall belong to the University if any one of the following circumstances exists:
			 			<ol type="a">
			 				<li>
			 					The invention was developed as part of the regularly-assigned duties of the inventor; 
			 				</li>
			 				<li>
			 					The invention was developed with direct or indirect support of funds administered by the University;
			 				</li>
			 				<li>
			 					The invention was developed making substantial use of the facilities or resources of the University;
			 				</li>
			 				<li>
			 					Commissioned work by the University or “Work for Hire”;
			 				</li>
			 				<li>
			 					The ownership of the invention was assigned by the inventor to the university;
			 				</li>
			 				<li>
			 					 In case of students ,the invention or work was developed when he/she received financial support from the University in the form of tuition waiver, scholarship, salary, stipend or allowance regardless of the amount; and
			 				</li>
			 				<li>
			 					Works whose inventors cannot be determined.
			 				</li>
			 			</ol>
			 		</p>
			 	</li>
			 	<li>
			 		<p><b><span>7.3</span> Externally Funded or Sponsored</b></p>
			 		<p>
			 			If the patentable invention arose or was related to an undertaking covered by distinct agreement of the inventor and a third party using funds not administered by  the University or  where the University has no claim of ownership under Section 7.2, the terms of the agreement will prevail over these policies. In the absence of any contrary provision in the agreement, these policies shall apply to externally funded or sponsored research.
			 		</p>
			 	</li>
			 	<li>
			 		<p><b><span>7.4</span> Student Works</b></p>
			 		<p>
			 			In the case of student works produced and submitted as course requirements, these works are owned by the students, unless these are university non-course requirements.The University may claim rights over the patent over works contributed by University employees in the course of their mentoring assignment as part of their regularly-assigned duties.  In cases not falling under section 7.2, the faculty member/s and the student/s concerned may enter into an agreement prior to the mentoring engagement specifying, among others, the ownership of the intellectual property and the sharing of royalties.  In case there is no agreement, the ownership of the patent shall belong to the student.
			 		</p>
			 		<p>
			 			Notwithstanding ownership by students of their works, the university or any member of the academic community is allowed, without infringing any intellectual property right, free access to and use of the student works to pursue or develop them for academic purposes.
			 		</p>
			 	</li>
			 	<li>
			 		<p><b><span>7.5</span> Reversion of Ownership</b></p>
			 		<p>
			 			In the event that the University decides not to claim ownership over the work, ownership of the IP rights shall revert to the author through an agreement. For government funded researches and works, Republict Act 10055 shall be applied.
			 		</p>
			 	</li>
			 	<li>
			 		<p>
			 			<b><span>7.6</span> Assignment of Patent</b>
			 			<ul>
			 				<li>
			 					<p><span><b>7.6.1</b></span> In cases covered by section 7.2, the inventor shall execute a deed of assignment of patent together with the disclosure form required under paragraph 6.2 of these policies in favor of the University.</p>
			 				</li>
			 				<li>
			 					<p><span><b>7.6.2</b></span> In cases where the inventor assigns the patent rights to the University, the University shall take care of the filing of the patent, the payment of the filing and maintenance fees.</p>
			 				</li>
			 			</ul>
			 		</p>
			 	</li>
			 	<li>
			 		<p>
			 			<b><span>7.7</span> Outside Consultants and Independent Contractors</b>
			 			<ul>
			 				<li>
			 					<p>
			 						<span><b>7.7.1</b></span> The University shall own the patent of any work produced by non-university employees who are contracted to perform certain work for the university as consultants or independent contractors, unless specified otherwise in a written agreement between the parties.
			 					</p>
			 				</li>
			 				<li>
			 					<p>
			 						<span><b>7.7.2</b></span> It is the obligation of employees, students and partners engaged in consulting and other activities with outside entities to ensure that their activities and agreements with third parties are not in conflict with the provisions of this Policy or other commitments involving the University. Employees, students and partners should inform those outside parties with whom they make agreements of their obligations to the University.
			 					</p>
			 				</li>
			 			</ul>
			 		</p>
			 	</li>
			 </ul>
