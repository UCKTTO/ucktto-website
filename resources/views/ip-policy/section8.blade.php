	<div class="ip-row">
		<div class="ip-container">
			<h3>SECTION 8</h3>
			<h4 style="text-align: center">OWNERSHIP OF COPYRIGHT AND <br>RELATED RIGHTS</h4>
			
			<ul>
				<li>
					<p><b><span>8.1</span> Owned by Author</b></p>
					<p>
						Rights to copyright shall belong to the author except in the instances stated in section 8.2 of these policies.
					</p>
					<p>
						In case of multiple authors, ownership shall be determined by stipulation of the parties. In    case there is no stipulation, section 8.2 applies.
					</p>
					<p>
						Notwithstanding ownership of the author of his works, the university or any member of the academic community is allowed, without infringing any intellectual property right, free access to and use of their works to pursue or develop them for academic purposes.
					</p>
				</li>
				<li>
					<p><b><span>8.2</span> Owned by the University</b></p>
					<p>
						Rights to Copyright shall belong to the University if any one of the following circumstances exist:
						<ol type="a">
							<li>
								The work was developed with direct or indirect support of funds administered by the University; 
							</li>
							<li>
								The work was done making substantial use of the facilities or resources of the University;
							</li>
							<li>
								Commissioned work by the University or “Work for Hire”;
							</li>
							<li>
								Teaching or training  materials or modules created as part of the regularly assigned duties of the university employee;
							</li>
							<li>
								The ownership of the copyright was assigned by the author to the university;
							</li>
							<li>
								In case of students, the work was developed or created when he/she received financial support from the University in the form of tuition waiver, scholarship, salary, stipend or allowance regardless of the amount; and
							</li>
							<li>
								Works, whose authorship cannot be determined;
							</li>
						</ol>
					</p>
				</li>
				<li>
					<p><b><span>8.3</span> Externally Funded or Sponsored Work</b></p>
					<p>
						Externally funded or sponsored work by authors and a third party where the University has no claim of ownership under Section 8.2 shall be covered by a special agreement.  The terms of the agreement will prevail over these policies. In the absence of any contrary provision in the agreement, these policies shall apply to externally funded or sponsored research. Government funded researches shall be governed by RA 10055.
					</p>
				</li>
				<li>
					<p><b><span>8.4</span> Student Works</b></p>
					<p>
						In the case of student works produced and submitted as course requirements, these works are owned by the students, unless these are university non-course requirements.   The University may claim  rights over the patent over works contributed by University employees in the course of their mentoring assignment as part of their regularly-assigned duties.  In cases not falling under section 8.2, the faculty member/s and the student/s concerned may enter into an agreement prior to the mentoring engagement specifying, among others, the ownership of the intellectual property and the sharing of royalties.  In case there is no agreement, the ownership of the patent shall belong to the student.
					</p>
					<p>
						Notwithstanding ownership by students of their works, the university or any member of the academic community is allowed, without infringing any intellectual property right, free access to and use of the student works to pursue or develop them for academic purposes.
					</p>
				</li>
				<li>
					<p><b><span>8.5</span> Reversion of Ownership</b></p>
					<p>
						In the event that the University decides not to claim ownership over the work, ownership of the IP rights shall revert to the author through an agreement. For government funded researches and works, Republict Act 10055 shall be applied.
					</p>
				</li>
				<li>
					<p>
						<b><span>8.6</span> Assignment of Copyright</b>
						<ul>
							<li>
								<p><span><b>8.6.1</b></span> In cases covered by section 8.2, the author shall execute a deed of 	assignment of copyright together with the disclosure form required under 	paragraph 6.2 of these policies in favour of the University.</p>
							</li>
							<li>
								<p><span><b>8.6.2</b></span> The University shall be responsible for submitting copies of the work to the National Library and Supreme Court Library or to the appropriate government agency.</p>
							</li>
						</ul>
					</p>
				</li>
				<li>
					<p>
						<b><span>8.7</span> Outside Consultants and Independent Contractors</b>
						<ul>
							<li>
								<p><span><b>8.7.1</b></span> The University shall own the copyright of any works produced by non-university employees who are contracted to perform work for the university as consultants or independent contractors, unless specified otherwise in a written agreement between the parties.</p>
							</li>
							<li>
								<p><span><b>8.7.2</b></span> It is the obligation of employees, students and partners engaged in consulting and other activities with outside entities to ensure that their activities and agreements with third parties are not in conflict with the provisions of this Policy or other commitments involving the University. Employees, students and partners should inform those outside parties with whom they make agreements of their obligations to the University</p>
							</li>
						</ul>
					</p>
				</li>
			</ul>
