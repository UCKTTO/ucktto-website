                 <h4>Section 13: Penalties</h4>
                 <p>Any person found to have violated any of the provisions of these policies shall be dealt with in accordance with the provisions of the faculty, student, staff, administrative or applicable employment manual/handbook and in accordance with the relevant provisions of the law.  All items in Section 10 shall be considered as major offenses.</p><br>

				 <h4>Section 14: Waiver of the IP Policy</h4>
                 <p>The University President, in his own initiative or through the recommendation of the University Committee on Intellectual Property (UCIP), shall have the sole discretion to waive or vary any or all of the provisions of these policies on a case-to-case basis if a strict application of these policies shall not result in fairness and equity in a particular case.  A waiver or variance of the intellectual property policies made in one case shall not serve as a precedent to any future or subsequent case.</p>  <br>

                 <h4>Section 15: Amendment or Revision</h4>   
                 <p>The provisions of this policy may be reviewed and amended on a yearly basis. Any changes to these policies may be initiated by the UC-KTTO, new IP related legislation and amendments on existing IP laws, or may be proposed by University employee or student through the following process:<br>a)	UC-KTTO staff or University employee/student shall write a letter addressed to the Vice President for Academics and Research specifying the proposed changes and the reasons for the proposed changes;<br>b)	Proposals will be reviewed by the UCIP and shall be approved by the Vice President for Academics and Research; and <br>c)	The KTTO will revise the UC IP Policy incorporating the approved changes or amendments on IP laws.</p><br>    

                 <h4>Section 16: Repealing Cause</h4>        
                 <p>All other University policies inconsistent with these policies are repealed accordingly.</p> <br>

                 <h4>Section 17: Effectivity Clause</h4>
                 <p>17.1  The UC IP Policy shall take effect immediately upon its approval by the Executive Council.<br>17.2  Non-Retroactive Effect. Agreements entered into by the University and the employee, student, or partner prior to the effectivity date shall be governed by the agreement, contract and other legally binding documents in effect at the time of the signing.</p>
