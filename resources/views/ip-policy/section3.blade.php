                <h4>Section 3: Legal Basis</h4><br>
                 <p>The Intellectual Property Code (IP Code) of the Philippines (Republic Act No. 8293), An Act amending certain provision of RA 8293 (RA 10372), Philippine Technology Transfer Act (Republic ACT No. 10055), Philippine Innovation Act (RA 11293), Innovative start-up Act (RA 11337) and other relevant laws are deemed written into the Guidelines. </p> <br>

                <h4>Section 4: Objectives</h4><br>
                 <p>Through this Policy, the University seeks:
                 	<br>a) To promote  development of useful Inventions, researches and original creations;
                 	<br>b) To promote self-expression among employees, students and partners of the University involved in literary, intellectual or artistic creation;
                 	<br>c) To provide advice to employees, students and partners on Intellectual Property rights and protection; and
                 	<br>d) To protect the rights of employees, students and partners to the products of their research efforts, the rights of Inventors over their inventions, and the rights of Creators over their works.</p><br>

                <h4>Section 5: Scope and Coverage of the Policy</h4>
                <p>5.1 This policy shall apply to all forms of university owned intellectual property created on or after the date of approval of this Policy by the Executive Council, and to all IP Rights associated with them. <br><br>5.2 This policy shall apply to employees, students, and partners who are undertaking research and/or creative activities. The policies cover all kinds of intellectual properties under the Intellectual Property Code of the Philippines (RA 8293) and related laws. <br><br> 5.3 Rights and obligations under this Policy shall survive any termination of enrollment or employment at the University. <br><br>5.4 This Policy may supplement the University’s rules on:<br>a)	Dishonesty and plagiarism;<br>b)	Conflict of interest;<br>c)	The University’s right to negotiate and meet its obligations;<br>d)	Moral and ethical behavior;<br>e)	Research and editorial policies of its constituent units; and <br>f)	Other rules deemed essential to its Mission and Vision, and character as a University.</p>
